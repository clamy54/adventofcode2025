    section text

start:
    lea     sequence,a0
    clr.l   d0
init_seq:
    move.b  d0,(a0)+    ; creation de la sequence 0-99
    addq.b  #1,d0
    cmp.b   #100,d0
    bne     init_seq

    move.w  #50,index       ; index pointe sur la valeur 50
    clr.w   score          

    lea     lignes,a0   ; on pointe sur le début des lignes

main_loop:

    tst.b   (a0)    ; si ligne vide c'est qu'on a fini
    beq     afficher_score

    move.b  (a0)+,d0        ; d0 = direction

    ; Convertir le nombre ASCII en valeur
    clr.w   d1              ; d1 = compteur
convert_num:
    move.b  (a0)+,d2
    beq    process_line    ; Si 0, fin du nombre
    sub.b   #48,d2         ; Convertir ASCII -> chiffre
    mulu    #10,d1
    add.w   d2,d1
    bra    convert_num

process_line:
    ; d0 = direction (L ou R)
    ; d1 = compteur
    ; a0 pointe après le 0 de fin de ligne

move_loop:

    cmp.b   #76,d0      ; Si L on va a depl_gauche
    beq    depl_gauche

depl_droite:
    move.w  index,d2
    cmp.w   #99,d2
    bne    right_normal
    clr.w  index   ; Si index = 99, on repart a 0
    bra    check_score
right_normal:
    addq.w  #1,index
    bra    check_score

depl_gauche:
    move.w  index,d2
    tst.w   d2
    bne    left_normal
    move.w  #99,index   ; Si index = 0, on va a 99
    bra    check_score
left_normal:
    subq.w  #1,index

check_score:
    tst.w   index        ; si index=0 alors on incremente le score
    bne     decr_compteur
    addq.w  #1,score

decr_compteur:
    subq.w  #1,d1
    tst.w   d1
    bne     move_loop
    bra     main_loop


afficher_score:
    pea     aff_score(pc)
    move.w  #9,-(sp)
    trap    #1
    addq.l  #6,sp

    clr.l   d0            ; correction bug affichage , on se fait pas chier on passe un coup de Monsieur Propre
    clr.l   d1
    clr.l   d2
    clr.l   d3
    movea.l #0,a0
    movea.l #0,a1
    movea.l #0,a2

    move.w  score,d0      ; Affichage du score
    bsr     print_num

    pea     crlf(pc)        ; Nouvelle ligne
    move.w  #9,-(sp)
    trap    #1
    addq.l  #6,sp

    move.w  #1,-(sp)        ; Attente appui touche
    trap    #1
    addq.l  #2,sp

    clr.w   -(sp)
    trap    #1           ; Hasta la vista baby


print_num:
    lea     num_buffer+11,a0
    move.l  a0,a1           ; Sauvegarder position
    move.l  #10,d2

    tst.w   d0
    bne     .conv_loop
    move.b  #48,-(a0)   ;  si 0 on affiche '0'
    move.b  #0,(a1)            ; null juste après
    bra    affiche

.conv_loop:
    tst.w   d0
    beq    .fin_conv

    divu    d2,d0          ; division par 10 pour extraire le chiffre
    swap    d0
    add.b   #48,d0
    move.b  d0,-(a0)
    clr.w   d0
    swap    d0
    bra    .conv_loop

.fin_conv:
    move.b  #0,(a1)        ; null à la position de départ (juste après le dernier chiffre)

affiche:
    move.l  a0,-(sp)
    move.w  #9,-(sp)
    trap    #1
    addq.l  #6,sp
    rts



