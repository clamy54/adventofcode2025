
    section text

start:
        move.l  #0,d7            ; d7 = score
        move.w  #1,d5            ; ligne de depart
        move.w  #142,largeur
        move.w  #141,hauteur     ; on s'arrete a l'avant derniere ligne

        move.w  largeur,largeurutile
        sub.w   #1,largeurutile         ; arret du parcours horizontal en largeur-1

loop_y:
        cmp.w   hauteur,d5
        bge     end_loop         ; quand d5(y)>=hauteur

        move.w  #1,d4             ; sinon on remet d4 Ã  1

loop_x:
        cmp.w   largeurutile,d4
        bge     next_line     ; quand d4(x)>=largeur-1      

        move.w  d5,d0           
        mulu.w  largeur,d0     
        add.w   d4,d0           ; d0=y*largeur+x
        lea     tableau,a0     
        move.b  0(a0,d0.w),d1   ; chargement du caractere en cours dans d1

        cmp.b   #64,d1          ; si ce n'est pas un @ on saute
        bne     next_x

check1:                         ; verif pour x-1,y
        eor     d6,d6           ; on met d6 a 0 pour y stocker le nb de voisins
        move.w  d0,d2
        sub.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check2
        add.b   #1,d6

check2:                          ; verif pour x+1,y
        move.w  d0,d2
        add.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check3
        add.b   #1,d6

check3:                          ; verif pour x,y-1
        move.w  d0,d2
        sub.w   largeur,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check4
        add.b   #1,d6

check4:                          ; verif pour x,y+1
        move.w  d0,d2
        add.w   largeur,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check5
        add.b   #1,d6

check5:                          ; verif pour x-1,y-1
        move.w  d0,d2
        sub.w   largeur,d2
        sub.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check6
        add.b   #1,d6

check6:                          ; verif pour x-1,y+1
        move.w  d0,d2
        add.w   largeur,d2
        sub.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check7
        add.b   #1,d6

check7:                          ; verif pour x+1,y-1
        move.w  d0,d2
        sub.w   largeur,d2
        add.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     check8
        add.b   #1,d6

check8:                           ; verif pour x+1,y+1
        move.w  d0,d2
        add.w   largeur,d2
        add.w   #1,d2
        move.b  0(a0,d2.w),d1
        cmp.b   #64,d1
        bne     verif
        add.b   #1,d6

verif:
        cmp.b   #4,d6       ; si moins de 4 voisins
        bge     next_x
        add.l   #1,d7     ; incremente le score    

next_x:
        add.w   #1,d4             ; on incremente x et on boucle
        bra     loop_x

next_line:
        add.w   #1,d5             ; on incremente y et on boucle
        bra     loop_y

end_loop:
        pea     aff_score(pc)   ; Affichage score
        move.w  #9,-(sp)      
        trap    #1
        addq.l  #6,sp


        move.l  d7,d0
        bsr     print_num

        pea     msg_crlf(pc)
        move.w  #9,-(sp)
        trap    #1
        addq.l  #6,sp

        move.w  #1,-(sp)        ; Attente appui touche
        trap    #1
        addq.l  #2,sp

        clr.w   -(sp)
        trap    #1              ; hasta la vista bye bye


print_num:
        movem.l d0-d3/a0-a1,-(sp) ; sauvegarde des registres sur la pile
        
        tst.l   d0                      ; si d0 = 0 on affiche 0
        bne     .not_zero              
        lea     numbuf,a0
        move.b  #48,(a0)+
        clr.b   (a0)
        pea     numbuf
        move.w  #9,-(sp)
        trap    #1
        addq.l  #6,sp
        bra     .fini
        
.not_zero:                      ; sinon
        lea     numbuf+11,a0
        clr.b   (a0)
        move.l  d0,d1

.conv_loop:

        moveq   #0,d2           
        move.l  d1,d3           
        divu.w  #10,d3        ; division par 10 
        move.w  d3,d1           ; on met le quotient dans d1 
        clr.w   d3              ; on efface les 16 bits bas
        swap    d3              ; on met le reste de la division (16bis hauts) dans les 16 bits bas
        
        add.b   #48,d3          ; convertion du chiffre en ascii (48+d3) et stockage
        move.b  d3,-(a0)       
        
        tst.l   d1              ; teste si quotient different de 0 alors on a encore des chiffres a traiter
        bne     .conv_loop

        move.l  a0,-(sp)        ; on affiche le chiffre
        move.w  #9,-(sp)
        trap    #1
        addq.l  #6,sp

.fini:
        movem.l (sp)+,d0-d3/a0-a1 ; restore les registes
        rts

